CREATE TABLE PHR_USERS_AUTH_METHODS(
  id BIGSERIAL PRIMARY KEY NOT NULL,
  auth_method VARCHAR(20),
  user_id BIGINT,
  status VARCHAR(20) DEFAULT 'ACTIVE' NOT NULL,
  created_date TIMESTAMP DEFAULT NOW() NOT NULL,
  created_by VARCHAR(50) DEFAULT 'PHR_WEB_APP' NOT NULL,
  updated_date TIMESTAMP DEFAULT NOW() NOT NULL,
  updated_by VARCHAR(50) DEFAULT 'PHR_WEB_APP' NOT NULL,
  CONSTRAINT PHR_USERS_AUTH_METHODS_UID_FK FOREIGN KEY(user_id) REFERENCES PHR_USERS(id) ON DELETE CASCADE
);

CREATE INDEX PHR_USERS_AUTH_METHODS_ID_IDX ON PHR_USERS_AUTH_METHODS (id);
CREATE INDEX PHR_USERS_AUTH_METHODS_UID_IDX ON PHR_USERS_AUTH_METHODS (user_id);